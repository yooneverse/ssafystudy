#DP는 너무 어렵네요... 제미나이 도움 씨게 받음
n = int(input())


#N의 최대 크기인 10000이라서 넉넉히 10001
# wine: 각 잔에 담긴 포도주 양. 계산의 편의를 위해 인덱스 1부터 사용합니다.
# dp: i번째 잔까지 고려했을 때 마실 수 있는 포도주의 최대 양.
wine = [0] * 10001
dp = [0] * 10001

# 다음 줄부터 n개의 포도주 양을 한 줄씩 입력받습니다.
for i in range(1, n + 1):
    wine[i] = int(input())

# 초기값 설정
# 1잔만 있을때는 1잔이 최대
dp[1] = wine[1]
# 잔이 두 개 이상일 경우 1번잔이랑 2번잔 모두 마시는 것이 최대
if n >= 2:
    dp[2] = wine[1] + wine[2]

# 동적 계획법 수행
# 걍 여기서는 아예 못세움 dp 식세우기 너무 어려움...
# 3번 잔부터는 연속 3잔을 마시는 건 불가
for i in range(3, n + 1):
    dp[i] = max(
        # 연속 3잔을 피하기 위해 이번 잔을 건너뛰는 경우입니다. 직전 잔(i-1)까지의 최대 양을 그대로 가져오기
        dp[i-1], 
                
        # 경우 2: 이번 잔(i)을 마시지만, 직전 잔(i-1)은 마시지 않는다.
        # 전전 잔(i-2)까지의 최대 양에 이번 잔(i)의 양을 더해줍니다.
        dp[i-2] + wine[i], 

        # 경우 3: 이번 잔(i)과 직전 잔(i-1)을 연속으로 마신다.
        # 연속 3잔 금지 규칙 때문에 전전 잔(i-2)은 절대 마실 수 없습니다.
        # 따라서 전전전 잔(i-3)까지의 최대 양에 직전 잔(i-1)과 이번 잔(i)의 양을 더해줍니다.
        dp[i-3] + wine[i-1] + wine[i])

# 결과 출력
print(dp[n])